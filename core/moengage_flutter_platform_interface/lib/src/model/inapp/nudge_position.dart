/// Position for Non-Intrusive Nudge Campaigns
/// @since 2.0.0
enum MoEngageNudgePosition {
  /// For nudge to be shown at the TOP
  top,

  /// For nudge to be shown at the BOTTOM
  bottom,

  /// For nudge to be shown at the BOTTOM_RIGHT
  bottomRight,

  /// For nudge to be shown at the BOTTOM_LEFT
  bottomLeft,

  /// For nudge to be shown at any available position
  any;
}
